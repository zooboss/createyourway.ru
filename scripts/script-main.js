$(document).ready(function()
{
    $(document).on('mouseenter', '.item', function()
    {
        $(this).css("background-color", "black");        
    });
    $(document).on('mouseout', '.item', function()
    {
        $(this).css("background-color", "deepskyblue");        
    });
    
            
    
            
        
                             
    

    
    
    
    
});